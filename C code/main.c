#include "stdio.h"
#include "string.h"
int main(){
    char a[1000];
    int i;
    for(i=0;i<1000;i++){
        a[i]=-1-i;
    }
    //128 + 127 = 255
    //-1 -2 -3 ... -127 -128 127 126 127 125 ... 3 2 1 0 -1 -2 ... -127 -128 127 ...
    printf("%d\n",strlen(a));
    return 0;
}

//巧记口诀:超出范围的数据如果是正数，则减去256;超出范围的数据如果是负数，则加上256.